function detectLPLocale() {
  var supported = Object.keys(LP_MESSAGES);
  var nav = (navigator.language || navigator.userLanguage || "en").toLowerCase();
  if (supported.indexOf(nav) !== -1) return nav;
  var primary = nav.split("-")[0];
  if (supported.indexOf(primary) !== -1) return primary;
  return "en";
}

function applyLPTranslations(locale) {
  var msgs = LP_MESSAGES[locale];
  if (!msgs) return;
  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var key = el.getAttribute("data-i18n");
    if (msgs[key] !== undefined) el.textContent = msgs[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach(function(el) {
    var key = el.getAttribute("data-i18n-html");
    if (msgs[key] !== undefined) el.innerHTML = msgs[key];
  });
  document.documentElement.lang = locale;
  document.documentElement.dir = locale === "ar" ? "rtl" : "ltr";
}

function initLP() {
  var locale = detectLPLocale();
  applyLPTranslations(locale);
  var sel = document.getElementById("lang-select");
  if (sel) {
    sel.value = locale;
    sel.addEventListener("change", function() { applyLPTranslations(this.value); });
  }
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLP);
} else {
  initLP();
}
