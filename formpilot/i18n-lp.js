// ── Locale Detection ────────────────────────────────────────────────
function detectLPLocale() {
  const supported = Object.keys(LP_MESSAGES);
  const nav = (navigator.language || navigator.userLanguage || "en").toLowerCase();

  // Exact match first (e.g. "en", "ja", "zh")
  if (supported.includes(nav)) return nav;

  // Match primary subtag (e.g. "en-US" -> "en", "zh-CN" -> "zh", "pt-BR" -> "pt")
  const primary = nav.split("-")[0];
  if (supported.includes(primary)) return primary;

  // Default to English
  return "en";
}

// ── Apply Translations ──────────────────────────────────────────────
function applyLPTranslations(locale) {
  const msgs = LP_MESSAGES[locale] || LP_MESSAGES.en;

  // Handle data-i18n (textContent)
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    if (msgs[key] !== undefined) {
      el.textContent = msgs[key];
    }
  });

  // Handle data-i18n-html (innerHTML) — for keys containing HTML markup
  document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
    const key = el.getAttribute("data-i18n-html");
    if (msgs[key] !== undefined) {
      el.innerHTML = msgs[key];
    }
  });

  // Set document lang attribute
  document.documentElement.lang = locale;

  // Set dir attribute for RTL languages
  document.documentElement.dir = locale === "ar" ? "rtl" : "ltr";
}

// ── Initialization ──────────────────────────────────────────────────
function initLP() {
  var locale = detectLPLocale();

  // Apply translations
  applyLPTranslations(locale);

  // Set up language selector if present
  var selector = document.getElementById("lang-select");
  if (selector) {
    selector.value = locale;
    selector.addEventListener("change", function () {
      var newLocale = this.value;
      applyLPTranslations(newLocale);
      // Update selector value in case it was changed programmatically
      this.value = newLocale;
    });
  }
}

// ── Auto-init ───────────────────────────────────────────────────────
if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLP);
} else {
  initLP();
}
